#Dada a lista `numeros = [1, 2, 3, 4, 5]`, multiplique cada elemento por 2.
numeros = [1, 2, 3, 4, 5]

for i in range(len(numeros)):
    numeros[i] = numeros[i] * 2
print((numeros))

#range > gera a sequência uma numérica dentro de um intervalo determinado
#len >  é utilizada para obter o número de itens, contagem